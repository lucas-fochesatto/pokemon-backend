class Pokemon {
  constructor(id, name, type, hp, attack, defense, speed, moves, status) {
    this.id = id;
    this.name = name;
    this.type = type;
    this.hp = hp;
    this.attack = attack;
    this.defense = defense;
    this.speed = speed;
    this.moves = moves;
    this.status = status;
  }
}